/**
 * op_add - adds a and b
 *
 * @a: input 1
 * @b: input 2
 *
 * Return: addition
*/

int op_add(int a, int b)
{
return (a + b);
}


/**
 * op_sub - difrence of a and b
 *
 * @a: input 
 * @b: input 
 *
 * Return: difference a and b
*/
int op_sub(int a, int b)
{
return (a - b);
}


/**
 * op_mul - multiplies a and p
 * @a: input 
 * @b: input 
 *
 * Return: product of two
*/
int op_mul(int a, int b)
{
return (a * b);
}


/**
 * op_div - divides tow values
 * @a: input 
 * @b: input 
 * Return: divition
*/
